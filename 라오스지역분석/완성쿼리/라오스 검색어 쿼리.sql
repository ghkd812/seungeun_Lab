USE Diablo

SELECT KEYWORD, COUNT(*) AS 총갯수
FROM VGLOG..KEYWORD_LOG
WHERE(KEYWORD LIKE '%라오스%'
OR KEYWORD LIKE '%방비엥%'
OR KEYWORD LIKE '%블루라군%'
OR KEYWORD LIKE '%비엔티엔%'
OR KEYWORD LIKE '%루앙프라방%'
OR KEYWORD LIKE '%쾅시폭포%'
OR KEYWORD LIKE '%왓시사켓%'
OR KEYWORD LIKE '%탓루앙%'
OR KEYWORD LIKE '%라오%'
OR KEYWORD LIKE '%쏭강%'
OR KEYWORD LIKE '%비엔티안%')
AND NEW_DATE >= '2016-01-01'
AND NEW_DATE < '2017-01-01'
GROUP BY KEYWORD
ORDER BY COUNT(*) DESC
